# output 5 hello
print("-" * 30)
for i in range(5):
    print("hello")

# output 0 ~ 4
print("-" * 30)
for i in range(5):
    print("1=", i)

# output 2 ~ 5 numbers
print("-" * 30)
for i in range(2, 6):
    print("1=", i)

# output 2 ~ 8, with 2 interval
print("-" * 30)
for i in range(2, 10, 2):
    print("1=", i)
